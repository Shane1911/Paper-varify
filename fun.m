function [ Y ] = fun( X );
global alfa A Zn F K_oj K_ij f D omega_i m I dm rate_ad
    A_1j=A*sind(alfa)+X(5);
    A_2j=A*cosd(alfa);
    cos_1j=X(2)/((f(1)-0.5)*D+X(3));
    sin_1j=X(1)/((f(1)-0.5)*D+X(3));
    cos_2j=(A_2j-X(2))/((f(2)-0.5)*D+X(4));
    sin_2j=(A_1j-X(1))/((f(2)-0.5)*D+X(4));
%     gama=D/dm;
%     tan_bj=sin_1j/(cos_1j+gama);
%     cos_bj=cos(atan(tan_bj));
%     sin_bj=tan_bj*cos_bj;
%     omega_cj=omega_i*(1-gama*cos_2j)/(1+cos_2j*cos_1j+sin_1j*sin_2j);
%     omega_Rj=-omega_i/(((cos_1j+tan_bj*sin_1j)/(1+gama*cos_1j)+(cos_2j+tan_bj*sin_2j)/(1-gama*cos_2j))*gama*cos_bj);
    omega_cj=rate_ad*0.5*omega_i*(1-D*cosd(alfa)/dm);
    alfa_j=atan(sind(alfa)/(cosd(alfa)+D/dm));
    omega_Rj=0.5*omega_i*dm/D*(1-(D*cosd(alfa)/dm)^2);
    Q_1j=K_oj*X(3)^1.5;%单位/N
    Q_2j=K_ij*X(4)^1.5;%单位/N
    F_cj=0.5*dm*1e-3*m*omega_cj^2;
    M_gj=I*omega_Rj*omega_cj*sin(alfa_j);
    F_1j=M_gj*1e3/D;
    F_2j=M_gj*1e3/D;
    Y(1,1)=-Q_1j*sin_1j+Q_2j*sin_2j-F_1j*cos_1j+F_2j*cos_2j;
    Y(2,1)=-Q_1j*cos_1j+Q_2j*cos_2j+F_1j*sin_1j-F_2j*sin_2j+F_cj;
    Y(3,1)=(A_1j-X(1))^2+(A_2j-X(2))^2-((f(2)-0.5)*D+X(4))^2;
    Y(4,1)=X(1)^2+X(2)^2-((f(1)-0.5)*D+X(3))^2;
    Y(5,1)=F-Zn*(Q_2j*sin_2j+F_2j*cos_2j);
end
